let a = 10;
let b = 3;
let str = "Hello";

console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulus:", a % b);
console.log("Exponentiation:", a ** b);

console.log("Addition with string:", a + str);
console.log("Subtraction with string:", a - str);
console.log("Multiplication with string:", a * str);
console.log("Division with string:", a / str);
console.log("Modulus with string:", a % str);

console.log("String + String:", str + str);
console.log("String * String:", str * str);
